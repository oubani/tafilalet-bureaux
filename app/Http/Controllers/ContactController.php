    public function store(Request $request)
    {
        //
        $request->validate([
            'name' => 'required|string|min:4',
            'email' => 'required|email',
            'message' => 'required|string'
        ]);
        DB::table('contacts')
            ->insert([
                'name' => $request->name,
                'email' => $request->email,
                'message' => $request->message,
            ]);

